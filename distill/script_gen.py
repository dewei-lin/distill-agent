def _tmpl_cols(tmpl: str) -> list[str]:
    """Return column names referenced by a match template."""
    import re
    refs = re.findall(r"\{(\w+)\}", tmpl)
    return refs if refs else [tmpl]


# ---------------------------------------------------------------------------
# Runtime dependency resolution (shared by the script docstring and the
# requirements.txt / environment.yml emitter)
# ---------------------------------------------------------------------------

# Minimum Python the generated script supports. Uses walrus (3.8+),
# `X | Y` type strings, f-strings; pinned to the project floor of 3.10.
_PY_REQUIRES = ">=3.10"
_PY_TESTED = "3.10–3.12"

# (package, pin, tested_version|None, why). tested_version is only set for
# packages whose end-to-end behaviour was actually verified at this version.
_PKG_PANDAS = ("pandas", ">=2.1", "2.3.3", "dataframe engine (always required)")
_PKG_NUMPY = ("numpy", ">=1.26", "2.2.6", "numeric arrays (always required)")
_PKG_SKLEARN = ("scikit-learn", ">=1.4", None, "MICE (iterative regression) imputation")
_PKG_PYARROW = ("pyarrow", ">=14.0", "24.0.0", "clean.parquet output")
_PKG_DATASETS = ("datasets", ">=2.18", "4.8.5", "HuggingFace Dataset (clean_dataset/)")
_PKG_PILLOW = ("Pillow", ">=10.0", "12.1.1", "decode companion images for HF Dataset")


def _runtime_packages(state: "PipelineState") -> list[tuple[str, str, str | None, str]]:
    """Return the exact package set the *generated script* needs to run.

    Conditional on what the script actually does: scikit-learn only when the
    session imputed, pyarrow only for parquet output, datasets+Pillow only for
    HF Dataset output. Pandas/numpy are always required.
    """
    cards = list(state.log)
    needs_mice = any(
        (c.action_taken or c.default_action) == "regression_impute"
        and c.stage == Stage.MISSINGNESS
        for c in cards
    )
    fmts = set(getattr(state, "output_formats", ["csv"]) or ["csv"])
    pkgs: list[tuple[str, str, str | None, str]] = [_PKG_PANDAS, _PKG_NUMPY]
    if needs_mice:
        pkgs.append(_PKG_SKLEARN)
    if "parquet" in fmts:
        pkgs.append(_PKG_PYARROW)
    if "hf" in fmts:
        pkgs.append(_PKG_DATASETS)
        if bool(state.companions):
            pkgs.append(_PKG_PILLOW)
    return pkgs


def emit_requirements(state: "PipelineState", session: "Session") -> dict[str, Path]:
    """Write `requirements.txt` and `environment.yml` next to the script.

    Documents the minimal, standalone dependency set for clean_script.py —
    pip pins plus a conda recipe — so a user with almost nothing installed can
    reproduce the run with a single command. Returns the written paths.
    """
    pkgs = _runtime_packages(state)
    name_w = max(len(f"{n}{pin}") for n, pin, _, _ in pkgs)

    req: list[str] = []
    r = req.append
    r("# ============================================================")
    r(f"# requirements.txt — runtime dependencies for clean_script.py")
    r(f"# Generated by Distill Agent (session {state.session_id}).")
    r("# ============================================================")
    r("# This script is STANDALONE — it needs only the packages below, not the")
    r("# full distill-agent project. Written for a fresh environment.")
    r("#")
    r(f"# Python {_PY_REQUIRES}   (tested on {_PY_TESTED})")
    r("#")
    r("# Quick start (pip + venv):")
    r("#     python -m venv .venv")
    r("#     source .venv/bin/activate        # Windows: .venv\\Scripts\\activate")
    r("#     pip install -r requirements.txt")
    r("#     python clean_script.py <your_input>")
    r("#")
    r("# Pins below are MINIMUMS; the 'tested' note records a known-good version.")
    r("# ------------------------------------------------------------")
    for n, pin, tested, why in pkgs:
        spec = f"{n}{pin}"
        note = f"tested {tested} — {why}" if tested else why
        r(f"{spec:<{name_w}}  # {note}")
    req_text = "\n".join(req) + "\n"

    env: list[str] = []
    e = env.append
    e("# Conda environment for clean_script.py — generated by Distill Agent.")
    e("# Create, activate, run:")
    e("#     conda env create -f environment.yml")
    e("#     conda activate distill-clean")
    e("#     python clean_script.py <your_input>")
    e("name: distill-clean")
    e("channels:")
    e("  - conda-forge")
    e("dependencies:")
    e(f"  - python{_PY_REQUIRES},<3.13")
    e("  - pip")
    e("  - pip:")
    for n, pin, _, _ in pkgs:
        e(f"      - {n}{pin}")
    env_text = "\n".join(env) + "\n"

    req_path = session.requirements_txt
    env_path = session.environment_yml
    req_path.write_text(req_text, encoding="utf-8")
    env_path.write_text(env_text, encoding="utf-8")
    return {"requirements": req_path, "environment": env_path}


