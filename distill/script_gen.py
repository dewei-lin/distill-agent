_MICE_HELPER = '''\
def _mice_impute(df: pd.DataFrame, var: str, random_state: int = 42) -> pd.DataFrame:
    """Self-contained MICE (iterative regression) imputation for one column.

    Numeric target: IterativeImputer (BayesianRidge, 10 rounds) fitted over
    all numeric columns. MICE is numeric-only, so a non-numeric target falls
    back to mode imputation — matching the session's categorical default.
    """
    out = df.copy()
    if not pd.api.types.is_numeric_dtype(out[var]):
        modes = out[var].mode(dropna=True)
        out[var] = out[var].fillna(modes.iloc[0] if len(modes) else None)
        return out
    from sklearn.experimental import enable_iterative_imputer  # noqa: F401
    from sklearn.impute import IterativeImputer

    num = [c for c in out.columns if pd.api.types.is_numeric_dtype(out[c])]
    imp = IterativeImputer(random_state=random_state, max_iter=10)
    out[num] = imp.fit_transform(out[num])
    return out
'''


def _emit_load_source(state: "PipelineState", lines: list[str]) -> None:
    """Emit _find_companion_dir() and _load_source() helpers."""
    has_companions = bool(state.companions)
    a = lines.append

    if has_companions:
        a("def _find_companion_dir(root: Path) -> 'Path | None':")
        a("    \"\"\"Return the directory under root that contains the most PNG files.\"\"\"")
        a("    from collections import Counter")
        a("    _pngs = list(root.rglob('*.png'))")
        a("    if not _pngs:")
        a("        return None")
        a("    return Counter(p.parent for p in _pngs).most_common(1)[0][0]")
        a("")
        a("")

    a("def _load_source(")
    a("    source: 'str | Path | pd.DataFrame',")
    a("    companion_dir: 'str | Path | None' = None,")
    a(") -> 'tuple[pd.DataFrame, Path | None]':")
    a('    """Read raw data from a file path, ZIP archive, or DataFrame."""')
    a("    if isinstance(source, pd.DataFrame):")
    a("        return source.copy(), Path(companion_dir) if companion_dir else None")
    a("    _p = Path(source)")
    a("    if _p.suffix.lower() == '.zip':")
    a("        _tmp = Path(tempfile.mkdtemp())")
    a("        with zipfile.ZipFile(_p) as _zf:")
    a("            _zf.extractall(_tmp)")
    a("        _candidates = list(_tmp.rglob(_SOURCE_FILENAME))")
    a("        if not _candidates:")
    a("            _candidates = sorted(_tmp.rglob('*.csv'))")
    a("        if not _candidates:")
    a("            raise FileNotFoundError(f'No CSV found inside {_p}.')")
    a("        # Prefer the CSV whose siblings include a target/label file (train split).")
    a("        def _priority(p: Path) -> int:")
    a("            siblings = {s.name.lower() for s in p.parent.iterdir()}")
    a("            return 0 if any(k in s for s in siblings")
    a("                           for k in ('train', 'dose', 'target', 'label')) else 1")
    a("        _main = sorted(_candidates, key=_priority)[0]")
    a("        if companion_dir is None:")
    if has_companions:
        a("            companion_dir = _find_companion_dir(_tmp) or _main.parent")
    else:
        a("            companion_dir = _main.parent")
    a("        return pd.read_csv(_main, dtype=object, keep_default_na=True), Path(companion_dir)")
    a("    _ext = _p.suffix.lower()")
    a("    if _ext in ('.xlsx', '.xls'):")
    a("        _df = pd.read_excel(_p, dtype=object)")
    a("    elif _ext == '.parquet':")
    a("        _df = pd.read_parquet(_p)")
    a("    elif _ext == '.json':")
    a("        _df = pd.read_json(_p, dtype=object)")
    a("    elif _ext in ('.sav',):")
    a("        _df = pd.read_spss(_p)")
    a("    elif _ext in ('.dta',):")
    a("        _df = pd.read_stata(_p)")
    a("    else:")
    a("        _df = pd.read_csv(_p, dtype=object, keep_default_na=True)")
    if has_companions:
        a("    if companion_dir is None:")
        a("        companion_dir = _find_companion_dir(_p.parent) or _p.parent")
    a("    return _df, Path(companion_dir) if companion_dir else None")
    a("")
    a("")


def _emit_to_hf_dataset(lines: list[str]) -> None:
    """Emit the to_hf_dataset() helper for image companions."""
    a = lines.append
    a("_PIPELINE_COLS = {")
    a("    'image_path', 'audio_path', 'text_path', 'video_path',")
    a("    'companion_path',")
    a("    'has_image', 'has_audio', 'has_text', 'has_video', 'has_companion',")
    a("}")
    a("")
    a("")
    a("def to_hf_dataset(")
    a("    df: pd.DataFrame,")
    a("    *,")
    a("    image_col: str = 'image_path',")
    a("    target_col: 'str | None' = None,")
    a(") -> 'datasets.Dataset':")
    a('    """Convert cleaned DataFrame to a HuggingFace Dataset with a native Image column.')
    a("")
    a("    Strips internal pipeline columns (image_path, has_image, companion_path,")
    a("    has_companion, etc.) so the HF Dataset contains only real features plus")
    a("    the 'image' column (PIL, decoded lazily from the original PNG bytes).")
    a("")
    a("    Requires: pip install datasets Pillow")
    a('    """')
    a("    import datasets")
    a("    from datasets import Image as HFImage")
    a("    out = df.copy()")
    a("    if image_col in out.columns:")
    a("        out = out.rename(columns={image_col: 'image'})")
    a("    out = out.drop(columns=[c for c in _PIPELINE_COLS if c in out.columns])")
    a("    if target_col and target_col in out.columns:")
    a("        out['label'] = pd.Categorical(out[target_col]).codes")
    a("        out = out.drop(columns=[target_col])")
    a("    for col in out.select_dtypes(include='object').columns:")
    a("        if col != 'image':")
    a("            try:")
    a("                out[col] = pd.to_numeric(out[col])")
    a("            except (ValueError, TypeError):")
    a("                pass")
    a("    ds = datasets.Dataset.from_pandas(out, preserve_index=False)")
    a("    if 'image' in ds.column_names:")
    a("        ds = ds.cast_column('image', HFImage())")
    a("    return ds")
    a("")
    a("")


def _emit_companion_constants(state: "PipelineState", lines: list[str], session: "Session | None" = None) -> None:
    """Emit the companion row-key helper (before clean()).

    Never emits a companion_manifest.json dependency. Companion filenames are
    fully determined by the key columns and a fixed extension — paths are
    derived and existence-checked at runtime inside match_companions().
    """
    if not state.companions:
        return
    tmpl = getattr(state, "companion_match_template", None)
    a = lines.append
    mime_types = sorted({c.mime_type for c in state.companions})

    a(f"# {'=' * 60}")
    a(f"# Companion files  ({len(state.companions)} file(s), {', '.join(mime_types)})")
    if tmpl:
        a(f"# Filename rule: {tmpl!r} + extension (e.g. .png)")
        a("# Paths are derived directly from key columns — no sidecar file needed.")
    else:
        a("#")
        a("# No match template was configured during cleaning.")
        a("# Pass match_col to match_companions() to specify the key column.")
    a(f"# {'=' * 60}")
    a("")

    if tmpl and "{" in tmpl and "}" in tmpl:
        import re as _re
        cols = _re.findall(r"\{(\w+)\}", tmpl)
        parts = []
        segments = _re.split(r"\{\w+\}", tmpl)
        for i, seg in enumerate(segments):
            if seg:
                parts.append(_pyrepr(seg))
            if i < len(cols):
                parts.append(f'df[{_pyrepr(cols[i])}].fillna("").astype(str)')
        a("def _companion_row_keys(df: pd.DataFrame) -> pd.Series:")
        a(f'    """Derive the companion match key for each row using template {tmpl!r}."""')
        a(f"    return {' + '.join(parts)}")
        a("")
    a("")


def _emit_match_companions(state: "PipelineState", lines: list[str]) -> None:
    """Emit the match_companions() helper function.

    Uses direct path construction + Path.exists() — never a glob scan or
    sidecar manifest. has_companion / has_image are set from actual file
    existence at runtime.
    """
    if not state.companions:
        return
    tmpl = getattr(state, "companion_match_template", None)
    is_compound = tmpl and "{" in tmpl and "}" in tmpl
    a = lines.append

    # Infer the dominant extension from the companions recorded in the session.
    from collections import Counter
    ext_counts: Counter = Counter(
        Path(c.path).suffix.lower() for c in state.companions
    )
    dominant_ext = ext_counts.most_common(1)[0][0] if ext_counts else ".png"

    a("def match_companions(")
    a("    df: pd.DataFrame,")
    a("    companion_dir: 'str | Path',")
    a("    *,")
    a(f"    extensions: 'list[str] | None' = None,")
    a(") -> pd.DataFrame:")
    a('    """Add companion path columns by checking existence of derived filenames.')
    a("")
    a("    For each row constructs the expected filename as")
    if tmpl:
        a(f"    {tmpl!r} + extension (default {dominant_ext!r}),")
    else:
        a("    <match_col_value> + extension,")
    a("    checks whether that file exists in companion_dir, and adds:")
    a("      <type>_path  — absolute path string, or None when file not found")
    a("      has_<type>   — True / False")
    a("")
    a("    No sidecar file required — paths derived entirely from data columns.")
    a('    """')
    a("    _IMAGE = {'.png', '.jpg', '.jpeg', '.tiff', '.tif'}")
    a("    _AUDIO = {'.wav', '.mp3', '.flac'}")
    a("")
    a("    def _prefix(ext: str) -> str:")
    a("        e = ext.lower()")
    a("        if e in _IMAGE: return 'image'")
    a("        if e in _AUDIO: return 'audio'")
    a("        if e == '.txt': return 'text'")
    a("        return e.lstrip('.')")
    a("")
    a(f"    if extensions is None:")
    a(f"        extensions = [{dominant_ext!r}]")
    a("    companion_dir = Path(companion_dir)")
    if is_compound:
        a("    keys = _companion_row_keys(df)")
    else:
        key_col = tmpl if tmpl else "match_col"
        a(f"    keys = df[{_pyrepr(key_col)}].fillna('').astype(str)")
    a("    out = df.copy()")
    a("    for ext in extensions:")
    a("        prefix = _prefix(ext)")
    a("        paths = [")
    a("            str(p) if (p := companion_dir / f\"{k}{ext}\").exists() else None")
    a("            for k in keys")
    a("        ]")
    a("        out[f\"{prefix}_path\"] = paths")
    a("        out[f\"has_{prefix}\"] = [p is not None for p in paths]")
    a("    return out")
    a("")
    a("")


def _pyrepr(value: Any) -> str:
    """A safe Python literal for the value (uses json for nested structures)."""
    if isinstance(value, (dict, list)):
        return json.dumps(value, ensure_ascii=True)
    return repr(value)
