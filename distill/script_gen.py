"""Output artifact (e) — the reproducible cleaning script.

Generates a standalone Python script (`clean_script.py`) that replays the
entire cleaning session on the raw input, with no dependency on the agent
or on an LLM. The script is plain `pandas` / `numpy` / `scikit-learn`
code — readable, editable, and deterministic.

The script is emitted from the decision log: every resolved DecisionCard
becomes one explicit, commented code block. Reading the script top to
bottom is reading exactly what the agent did and why.

Design:
  * Format-resolution decisions emit explicit pandas (rename, strip,
    sentinel-to-NaN, type coercion, label normalisation).
  * Missingness decisions emit drop / dropna / fillna; MICE (iterative
    regression) imputation emits a small self-contained scikit-learn block.
  * Outlier decisions emit fence-based clip / mask code from the recorded
    bounds.
  * Duplicate decisions emit drop_duplicates.
  * No-op decisions (leave_as_is, inspect, skipped) emit a comment only,
    so the audit trail stays complete.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import TYPE_CHECKING, Any

