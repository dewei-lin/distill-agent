def _guard_col(lines: list[str], var: str) -> list[str]:
    """Wrap lines in an if-column-exists guard.

    All per-column transformations are guarded so the script runs safely on
    test/holdout sets that have a different schema (missing target, dropped
    columns, etc.) without crashing.
    """
    return [
        f"if {_pyrepr(var)} in df.columns:",
        *("    " + line for line in lines),
    ]


# Keep the old name as an alias so external callers aren't broken.
_guard_target = _guard_col


def _emit_card(card, target_col: str | None = None) -> list[str]:
    """Return the lines of code (un-indented) for one decision card.

    Every per-column transformation is wrapped in ``if '<col>' in df.columns:``
    so the script is safe to run on test/holdout sets that may have a different
    schema (e.g. missing target variable, or columns that were dropped during
    cleaning and are therefore absent from the holdout set).
    """
    cid = card.card_id
    action = card.action_taken or card.default_action
    var = card.variable

    def _g(lines: list[str]) -> list[str]:
        """Guard lines behind a column-existence check for any named variable."""
        if var:
            return _guard_col(lines, var)
        return lines

    # ----- Format stage -----
    if cid == "format_column_names":
        renames = card.metadata.get("renames", {})
        return [
            f"# rename {len(renames)} column(s) to snake_case",
            f"df = df.rename(columns={_pyrepr(renames)})",
        ]
    if cid.startswith("format_strip_ws__"):
        return _g([
            f"# {var}: strip leading/trailing whitespace",
            f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].where("
            f"df[{_pyrepr(var)}].isna(), df[{_pyrepr(var)}].astype(str).str.strip())",
        ])
    if cid.startswith("format_missing_tokens__"):
        toks = card.metadata.get("tokens_found", [])
        return _g([
            f"# {var}: convert missing-value sentinel(s) {toks} to NaN",
            f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].replace({_pyrepr(toks)}, np.nan)",
        ])
    if cid.startswith("format_coerce_numeric__"):
        return _g([
            f"# {var}: coerce to numeric",
            f"df[{_pyrepr(var)}] = pd.to_numeric(df[{_pyrepr(var)}], errors='coerce')",
        ])
    if cid.startswith("format_coerce_datetime__"):
        return _g([
            f"# {var}: parse to datetime",
            f"df[{_pyrepr(var)}] = pd.to_datetime(df[{_pyrepr(var)}], errors='coerce')",
        ])
    if cid.startswith("format_coerce_boolean__"):
        return _g([
            f"# {var}: coerce to boolean",
            f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].map("
            "{'true': True, 't': True, 'yes': True, 'y': True, '1': True, 1: True,"
            " 'false': False, 'f': False, 'no': False, 'n': False, '0': False, 0: False}"
            ").astype('boolean') if df[{0}].dtype == object "
            "else df[{0}].astype('boolean')".format(_pyrepr(var)),
        ])
    if cid.startswith("format_normalize_labels__"):
        mapping = card.metadata.get("label_mapping", {})
        return _g([
            f"# {var}: normalize {len(mapping)} categorical label variant(s)",
            f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].replace({_pyrepr(mapping)})",
        ])

    # ----- Missingness stage -----
    if card.stage == Stage.MISSINGNESS and var is not None:
        info = card.metadata.get("applied_info", {})
        if action == "drop_variable":
            return _g([f"# {var}: drop variable (too sparse)",
                    f"df = df.drop(columns=[{_pyrepr(var)}], errors='ignore')"])
        if action == "drop_rows":
            return _g([f"# {var}: drop rows with missing value",
                    f"df = df.dropna(subset=[{_pyrepr(var)}])"])
        if action in ("median_impute", "mean_impute", "mode_impute"):
            val = info.get("imputed_value")
            return _g([f"# {var}: {action} (imputed value recorded by the session)",
                    f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].fillna({_pyrepr(val)})"])
        if action == "regression_impute":
            if "fallback_from" in info:
                val = info.get("imputed_value")
                return _g([
                    f"# {var}: {action} requested; fell back to "
                    f"{info.get('applied')} (sklearn unavailable at run time)",
                    f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].fillna({_pyrepr(val)})",
                ])
            return _g([f"# {var}: MICE (iterative regression) over the numeric feature space",
                    f"df = _mice_impute(df, {_pyrepr(var)})"])
        if action == "flag_missing_indicator":
            return _g([f"# {var}: add missing-value indicator column",
                    f"df[{_pyrepr(str(var) + '_was_missing')}] = df[{_pyrepr(var)}].isna()"])
        if action == "leave_as_is":
            return [f"# {var}: missing values left in place (analyst decision)"]

    # ----- Outlier stage -----
    if card.stage == Stage.OUTLIER and var is not None:
        bounds = card.metadata.get("bounds", {})
        lo = bounds.get("iqr_low")
        hi = bounds.get("iqr_high")
        if action in ("leave_as_is", "inspect"):
            return [f"# {var}: outliers reviewed, no change ({action})"]
        if action == "winsorize" and lo is not None:
            return _g([f"# {var}: winsorize at the 1.5xIQR fence",
                    f"df[{_pyrepr(var)}] = df[{_pyrepr(var)}].clip("
                    f"lower={lo!r}, upper={hi!r})"])
        if action == "treat_as_missing" and lo is not None:
            return _g([
                f"# {var}: treat fence outliers (and negatives) as missing",
                f"_m = (df[{_pyrepr(var)}] < {lo!r}) | (df[{_pyrepr(var)}] > {hi!r}) "
                f"| (df[{_pyrepr(var)}] < 0)",
                f"df.loc[_m.fillna(False), {_pyrepr(var)}] = np.nan",
            ])
        if action == "drop_rows" and lo is not None:
            return _g([
                f"# {var}: drop rows with fence-outlier values",
                f"_m = (df[{_pyrepr(var)}] < {lo!r}) | (df[{_pyrepr(var)}] > {hi!r})",
                f"df = df[~_m.fillna(False)]",
            ])

    # ----- Duplicate stage -----
    if cid == "duplicates_exact":
        key_cols = card.metadata.get("key_columns", [])
        if action == "remove_all_duplicates":
            return ["# remove exact-duplicate rows, keep first of each group",
                    f"df = df.drop_duplicates(subset={_pyrepr(key_cols)}, keep='first')"]
        if action == "drop_all_duplicate_rows":
            return ["# drop every row that participates in an exact duplicate",
                    f"df = df[~df.duplicated(subset={_pyrepr(key_cols)}, keep=False)]"]
        return ["# exact duplicates: kept as-is (analyst decision)"]
    if cid.startswith("duplicates_near"):
        if action in ("merge_keep_first", "merge_keep_last"):
            idx = card.metadata.get("row_indices", [])
            keep = idx[:1] if action == "merge_keep_first" else idx[-1:]
            drop = [i for i in idx if i not in keep]
            return [f"# entity-key collision: {action}",
                    f"df = df.drop(index={_pyrepr(drop)}, errors='ignore')"]
        return [f"# near-duplicate cluster: {action} (no row change)"]

    # ----- Feature Engineering (Binning, Scaling, Encoding) -----
    if card.stage in (Stage.BINNING, Stage.SCALING, Stage.ENCODING):
        if action == "leave_as_is":
            return [f"# {var}: kept as-is (no {card.stage.value} applied)"]
        new_col = card.metadata.get("new_col") or card.metadata.get(
            "applied_info", {}
        ).get("new_col")
        if card.stage == Stage.BINNING and new_col:
            cut_points = (
                card.metadata.get("applied_info", {}).get("cut_points")
                or card.metadata.get("cut_points")
            )
            n_actual = len(cut_points) - 1 if cut_points else card.metadata.get("n_bins", 4)
            method = action  # e.g. "bin_quantile"
            if cut_points:
                from .binning import make_bin_labels
                labels = make_bin_labels([float(x) for x in cut_points])
                return _g([
                    f"# {var}: {method} bin into {n_actual} groups → '{new_col}'",
                    # Coerce to numeric first so binning is robust even if an
                    # earlier coercion step did not run on this column.
                    f"df[{_pyrepr(new_col)}] = pd.cut(",
                    f"    pd.to_numeric(df[{_pyrepr(var)}], errors='coerce'), bins={cut_points!r},",
                    f"    labels={labels!r}, include_lowest=True",
                    f").astype('category')",
                ])
            # fallback when cut_points were not recorded (should not happen)
            return _g([
                f"# {var}: bin ({method}) into {n_actual} groups → '{new_col}'",
                f"df[{_pyrepr(new_col)}] = pd.cut(pd.to_numeric(df[{_pyrepr(var)}], errors='coerce'), "
                f"bins={n_actual!r}, "
                f"labels=[f'bin_{{i+1}}' for i in range({n_actual!r})], include_lowest=True)",
            ])
        if card.stage == Stage.SCALING and new_col:
            method = action  # e.g. "scale_standard"
            if method == "scale_standard":
                return _g([
                    f"# {var}: standardise (z-score) → '{new_col}'",
                    f"_mu, _sd = df[{_pyrepr(var)}].mean(), df[{_pyrepr(var)}].std(ddof=1)",
                    f"df[{_pyrepr(new_col)}] = (df[{_pyrepr(var)}] - _mu) / max(float(_sd), 1e-9)",
                ])
            if method == "scale_minmax":
                return _g([
                    f"# {var}: min-max scale to [0, 1] → '{new_col}'",
                    f"_lo, _hi = df[{_pyrepr(var)}].min(), df[{_pyrepr(var)}].max()",
                    f"df[{_pyrepr(new_col)}] = (df[{_pyrepr(var)}] - _lo) / max(float(_hi - _lo), 1e-9)",
                ])
            if method == "scale_robust":
                return _g([
                    f"# {var}: robust-scale (median / IQR) → '{new_col}'",
                    f"_med = df[{_pyrepr(var)}].median()",
                    f"_iqr = df[{_pyrepr(var)}].quantile(0.75) - df[{_pyrepr(var)}].quantile(0.25)",
                    f"df[{_pyrepr(new_col)}] = (df[{_pyrepr(var)}] - _med) / max(float(_iqr), 1e-9)",
                ])
            if method == "scale_log":
                return _g([
                    f"# {var}: log1p transform → '{new_col}'",
                    f"df[{_pyrepr(new_col)}] = np.log1p(df[{_pyrepr(var)}])",
                ])
        if card.stage == Stage.ENCODING:
            if action == "frequency_encode" and new_col:
                return _g([
                    f"# {var}: frequency encode → '{new_col}'",
                    f"_freq = df[{_pyrepr(var)}].value_counts(normalize=True)",
                    f"df[{_pyrepr(new_col)}] = df[{_pyrepr(var)}].map(_freq).fillna(0.0)",
                ])
            if action == "datetime_decompose":
                components = card.metadata.get("components",
                    ["year", "month", "day", "dayofweek"])
                fe_lines = [f"# {var}: decompose datetime into components"]
                fe_lines.append(f"_dt = pd.to_datetime(df[{_pyrepr(var)}], errors='coerce')")
                for comp in components:
                    new_c = f"{var}_{comp}"
                    if comp == "is_weekend":
                        fe_lines.append(
                            f"df[{_pyrepr(new_c)}] = (_dt.dt.dayofweek >= 5).astype(int)"
                        )
                    else:
                        fe_lines.append(
                            f"df[{_pyrepr(new_c)}] = getattr(_dt.dt, {_pyrepr(comp)})"
                        )
                return _g(fe_lines)
        # FE fallback — note the transform for manual review
        return [f"# {var}: {card.stage.value} / {action} — reproduce manually if needed"]

    # ----- Companion matching (companion stage) -----
    # The actual data effects (row drop, path/indicator columns) are applied
    # right after match_companions() in clean(), using runtime path existence —
    # never a frozen subject-id list. Here we only emit a documenting comment so
    # the stage section records the decision.
    if cid == "companion_matching":
        n_unmatched = card.metadata.get("n_unmatched", 0)
        if action == "drop_rows":
            return [f"# companion matching: drop {n_unmatched} subject(s) with no "
                    "companion file (applied after match_companions, by file existence)"]
        if action == "add_indicator":
            return [f"# companion matching: {n_unmatched} subject(s) missing companion — "
                    "keep all rows, add has_<type> presence indicator"]
        if action == "flag_caveat":
            return [f"# companion matching: {n_unmatched} subject(s) missing companion — "
                    "noted in report, no rows dropped"]
        if action == "include_path":
            return ["# companion matching: complete coverage — companion path column kept"]
        if action == "exclude_path":
            return ["# companion matching: complete coverage — companion path column omitted"]
        return [f"# companion matching: {n_unmatched} subject(s) missing companion — "
                "kept (paths reconstructed on demand)"]

    # ----- fallback -----
    if card.status in (DecisionStatus.AGENT_AUTO, DecisionStatus.SKIPPED):
        return [f"# {card.stage.value}: {card.issue} (no data change)"]
    return [f"# {card.stage.value} / {cid}: action '{action}' — review manually"]


