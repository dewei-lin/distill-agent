def emit_script(state: "PipelineState", session: "Session") -> Path:
    """Write `clean_script.py` to the session directory. Returns its path."""
    lines: list[str] = []
    a = lines.append

    cards = list(state.log)
    needs_mice = any(
        (c.action_taken or c.default_action) == "regression_impute"
        and c.stage == Stage.MISSINGNESS
        for c in cards
    )

    has_companions = bool(state.companions)
    tmpl = getattr(state, "companion_match_template", None)

    # Which output formats the analyst selected after cleaning. state.output_formats
    # is set in finalize(); default to CSV only. Drives both the docstring's OUTPUT
    # section and the generated __main__ block below.
    fmts = set(getattr(state, "output_formats", ["csv"]) or ["csv"])
    has_images = any(
        Path(c.path).suffix.lower() in {".png", ".jpg", ".jpeg", ".tiff", ".tif"}
        for c in state.companions
    )

    # Companion output shaping. The companion path / presence columns are
    # runtime plumbing (reconstructed by match_companions); by default they are
    # kept OUT of the cleaned tabular output. The companion_matching card records
    # whether the analyst opted to keep the path column (include_path action) or
    # a presence indicator (add_indicator action).
    _comp_card = next((c for c in cards if c.card_id == "companion_matching"), None)
    _comp_action = (
        (_comp_card.action_taken or _comp_card.default_action) if _comp_card else None
    )
    companion_drop_rows = _comp_action == "drop_rows"
    include_companion_path = bool(
        _comp_card and _comp_card.metadata.get("include_companion_path")
    )
    include_companion_indicator = bool(
        _comp_card and _comp_card.metadata.get("include_companion_indicator")
    )
    # companion_path / has_companion aliases are always dropped (user feedback:
    # uninformative). <type>_path kept only when include_path; has_<type> kept
    # only when add_indicator.
    _drop_from_table: set[str] = {"companion_path", "has_companion"}
    if not include_companion_path:
        _drop_from_table |= {"image_path", "audio_path", "text_path", "video_path"}
    if not include_companion_indicator:
        _drop_from_table |= {"has_image", "has_audio", "has_text", "has_video"}

    # ----- header -----
    src_name = state.input_path.name
    a('"""Reproducible cleaning script — generated by Distill Agent.')
    a("")
    a(f"Session : {state.session_id}")
    a(f"Source  : {src_name}")
    a(f"Result  : {state.n_rows_in:,}x{state.n_cols_in} raw  ->  "
      f"{len(state.df):,}x{len(state.df.columns)} clean")
    a("")
    a("This script replays the whole cleaning session deterministically.")
    a("It uses only pandas / numpy" + (" / scikit-learn" if needs_mice else "")
      + " — no LLM, no agent.")
    a("")
    # --- explicit input assumptions ---
    a("=" * 60)
    a("WHAT THIS SCRIPT EXPECTS AS INPUT")
    a("=" * 60)
    a("")
    a(f"The raw structured data file is:  {src_name}")
    a(f"  ({state.n_rows_in:,} rows x {state.n_cols_in} columns)")
    a("")
    if has_companions:
        n_comp = len(state.companions)
        mime_types = sorted({c.mime_type for c in state.companions})
        sample_names = [c.path.name for c in state.companions[:5]]
        a(f"Companion files: {n_comp} file(s)  [{', '.join(mime_types)}]")
        a(f"  Sample names : {', '.join(sample_names)}"
          + (f"  … +{n_comp - 5} more" if n_comp > 5 else ""))
        if tmpl:
            a(f"  Match rule   : filename stem = {tmpl!r}")
            a(f"                 (derived from columns: "
              + ", ".join(_tmpl_cols(tmpl)) + ")")
        a("")
        a("─" * 60)
        a("HOW TO CALL clean()")
        a("─" * 60)
        a("")
        a("Option 1 — ZIP containing both the CSV and the companion files:")
        a(f"    The ZIP must have {src_name!r} at its root (or any subfolder).")
        a("    Companion files must be in the same directory as the CSV inside")
        a("    the ZIP.  clean() will unpack and find them automatically.")
        a("")
        a("    python clean_script.py  your_archive.zip")
        a("    # or in Python:")
        a('    from clean_script import clean')
        a('    df = clean("your_archive.zip")')
        a("")
        a(f"Option 2 — {src_name!r} and companion files already unpacked:")
        a(f"    python clean_script.py  {src_name}  /path/to/companion/dir/")
        a("    # or in Python:")
        a('    from clean_script import clean')
        a(f'    df = clean("{src_name}", companion_dir="/path/to/companion/dir/")')
        a("")
        a("Option 3 — Structured output only (no companions):")
        a(f"    python clean_script.py  {src_name}")
        a("    # or in Python:")
        a('    from clean_script import clean')
        a(f'    df = clean("{src_name}")')
        a("    # No companion path columns are present in this case.")
    else:
        a("─" * 60)
        a("HOW TO CALL clean()")
        a("─" * 60)
        a("")
        a(f"    python clean_script.py  {src_name}")
        a("    # or in Python:")
        a('    from clean_script import clean')
        a(f'    df = clean("{src_name}")')
        a("    # You may also pass a pandas DataFrame directly:")
        a('    df = clean(your_raw_dataframe)')
    a("")
    a("OUTPUT")
    a("──────")
    a("Running this script produces the format(s) selected during cleaning:")
    if "csv" in fmts:
        a("clean.csv    — the cleaned, model-ready structured dataset")
        if has_companions and tmpl:
            if include_companion_path:
                a("             includes a companion path column (e.g. 'image_path')")
                a("             with the resolved local path for each row (None if missing)")
            elif include_companion_indicator:
                a("             includes a presence indicator column (e.g. 'has_image');")
                a("             companion file paths are reconstructed on demand by the script")
            else:
                a("             companion file paths are reconstructed on demand and are")
                a("             kept out of the saved table (run with a companion dir to use them)")
    if "parquet" in fmts:
        a("clean.parquet — same data in Parquet format (typed, compressed; needs pyarrow)")
    if "hf" in fmts:
        a("clean_dataset/ — HuggingFace Dataset (load via datasets.load_from_disk);")
        if has_companions and has_images:
            a("             companion images embedded as PIL, decoded lazily from the")
            a("             original files — no separate image folder needed at train time")
        else:
            a("             typed Arrow table, feeds HF Trainer / PyTorch DataLoader")
    a("")
    # --- environment / setup ---
    a("─" * 60)
    a("SETUP / ENVIRONMENT")
    a("─" * 60)
    a("")
    _pkgs = _runtime_packages(state)
    a(f"Requires Python {_PY_REQUIRES}  (tested on {_PY_TESTED}).")
    a("This script is standalone — it needs only these packages:")
    for _n, _pin, _, _why in _pkgs:
        a(f"    {_n}{_pin:<8}  # {_why}")
    a("")
    a("Install everything (a requirements.txt and environment.yml are written")
    a("alongside this script):")
    a("    pip install -r requirements.txt")
    a("    # or with conda:")
    a("    conda env create -f environment.yml && conda activate distill-clean")
    a("")
    a("=" * 60)
    a('"""')
    a("")
    a("from __future__ import annotations")
    a("")
    a("import sys")
    a("import tempfile")
    a("import zipfile")
    a("from pathlib import Path")
    a("")
    a("import numpy as np")
    a("import pandas as pd")
    a("")
    a(f'_SOURCE_FILENAME = {_pyrepr(state.input_path.name)}')
    a("")
    a("")

    # ----- optional MICE helper -----
    if needs_mice:
        a(_MICE_HELPER)
        a("")

    # ----- companion manifest (emitted before clean() so clean() can reference it) -----
    _emit_companion_constants(state, lines, session=session)

    # ----- _load_source() helper -----
    _emit_load_source(state, lines)

    # ----- clean() function -----
    a("def clean(")
    a("    source: 'str | Path | pd.DataFrame',")
    a("    companion_dir: 'str | Path | None' = None,")
    a(") -> pd.DataFrame:")
    a('    """Full pipeline: load → clean → join companions.')
    a("")
    a("    source may be:")
    a(f"      - a ZIP file containing the raw CSV and companion files")
    a(f"      - the raw CSV / Excel / Parquet path")
    a(f"      - a raw pandas DataFrame (companion_dir must be supplied separately)")
    a("")
    if has_companions and tmpl:
        a("    When companions are present and a companion_dir is supplied, the")
        a("    returned DataFrame carries the resolved companion path / presence")
        a("    columns (e.g. image_path, has_image). The saved tabular output keeps")
        a("    only the columns selected during cleaning; the HF Dataset embeds the")
        a("    companion files directly. Pass companion_dir to override where")
        a("    companion files are searched.")
    a('    """')
    a("    df, companion_dir = _load_source(source, companion_dir)")
    a("")

    target_col = getattr(state, "target_column", None)
    for stage in Stage:
        stage_cards = [c for c in cards if c.stage == stage]
        if not stage_cards:
            continue
        a(f"    # {'=' * 60}")
        a(f"    # Stage: {stage.value}")
        a(f"    # {'=' * 60}")
        for c in stage_cards:
            for code_line in _emit_card(c, target_col=target_col):
                a("    " + code_line if code_line else "")
        a("")

    # companion join — sets image_path / has_image / companion_path / has_companion
    # via runtime path existence checks; no sidecar manifest needed
    if has_companions and tmpl:
        a("    # join companion file paths — existence checked at runtime, no sidecar needed")
        a("    if companion_dir is not None:")
        a("        df = match_companions(df, companion_dir)")
        if companion_drop_rows:
            a("        # drop rows whose companion file was not found at runtime")
            a("        # (matching is by file existence — robust to new/changed data)")
            a("        _has_cols = [c for c in df.columns if c.startswith('has_')]")
            a("        if _has_cols:")
            a("            df = df[df[_has_cols].any(axis=1)].reset_index(drop=True)")
        a("")

    a("    return df")
    a("")
    a("")

    # ----- match_companions() helper (emitted whenever companions are present) -----
    _emit_match_companions(state, lines)

    # ----- to_hf_dataset() helper (module-level) -----
    # Emit it once whenever it could be needed: either companions with images
    # exist (so the analyst can call it by hand) or HF output was selected.
    # Emitting at module level — never inside __main__ — keeps the script valid.
    if (has_companions and has_images) or "hf" in fmts:
        _emit_to_hf_dataset(lines)

    a('if __name__ == "__main__":')
    a('    _src           = sys.argv[1] if len(sys.argv) > 1 else _SOURCE_FILENAME')
    a('    _companion_dir = sys.argv[2] if len(sys.argv) > 2 else None')
    a('    cleaned = clean(_src, _companion_dir)')
    a('')

    # Tabular outputs (CSV / Parquet) drop the companion plumbing columns by
    # default; the HF Dataset path keeps the full frame so images can embed.
    tabular_strips = bool(has_companions and tmpl and _drop_from_table)
    if tabular_strips and ("csv" in fmts or "parquet" in fmts):
        a(f"    _DROP_FROM_TABLE = {sorted(_drop_from_table)!r}")
        a("    # Companion path/presence columns are runtime plumbing — kept out")
        a("    # of the saved tabular data unless explicitly requested.")
        a("    _table = cleaned.drop(")
        a("        columns=[c for c in _DROP_FROM_TABLE if c in cleaned.columns]")
        a("    )")
        table_var = "_table"
    else:
        table_var = "cleaned"

    if "csv" in fmts:
        a(f'    {table_var}.to_csv("clean.csv", index=False, lineterminator="\\n")')
        a(f'    print(f"Wrote clean.csv  ({{{table_var}.shape[0]:,}} rows × {{{table_var}.shape[1]}} cols)")')

    if "parquet" in fmts:
        a("    try:")
        a(f'        {table_var}.to_parquet("clean.parquet", index=False)')
        a('        print("Wrote clean.parquet  (typed, compressed)")')
        a("    except ImportError as _e:")
        a('        print(f"Skipped clean.parquet — {_e}. Install with: pip install pyarrow")')

    if "hf" in fmts:
        # Build a HuggingFace Dataset directly from the cleaned table. When
        # companion paths resolved, they are embedded (images as PIL, etc.);
        # otherwise a table-only Arrow dataset is written. Self-contained:
        # `python clean_script.py data.zip` is enough to go zip -> HF object.
        a("    try:")
        a("        _hf_col = next(")
        a("            (c for c in ('image_path', 'audio_path', 'text_path')")
        a("             if c in cleaned.columns),")
        a("            None)")
        a("        _has_companions = bool(_hf_col) and bool(cleaned[_hf_col].notna().any())")
        a("        ds = to_hf_dataset(cleaned, image_col=_hf_col or 'image_path')")
        a('        ds.save_to_disk("clean_dataset/")')
        a("        if _has_companions:")
        a("            _n = int(cleaned[_hf_col].notna().sum())")
        a('            print(f"Wrote clean_dataset/  ({len(ds):,} rows, {_n:,} companions embedded)")')
        a("        else:")
        a('            print(f"Wrote clean_dataset/  ({len(ds):,} rows, table-only — '
          'no companion files resolved)")')
        a("    except ImportError as _e:")
        a('        print(f"Skipped clean_dataset/ — {_e}. '
          'Install with: pip install datasets Pillow")')

    a("")

    text = "\n".join(lines)
    out = session.clean_script
    out.write_text(text, encoding="utf-8")
    return out


